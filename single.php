<?php
/**
 * The template for displaying all single posts
 *
 * @package CarnavalSF
 */

get_header();
?>

<main id="primary" class="site-main">
	<?php
	while ( have_posts() ) :
		the_post();
		?>

		<article id="post-<?php the_ID(); ?>" <?php post_class(); ?>>
			<?php
				get_template_part( 'template-parts/content' );
				the_post_navigation();
			?>
		</article>

	<?php endwhile; ?>
</main>

<?php
get_sidebar();
get_footer();
